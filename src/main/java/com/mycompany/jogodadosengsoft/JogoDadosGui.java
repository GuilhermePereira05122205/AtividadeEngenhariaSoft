    private void inserirNomeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inserirNomeActionPerformed
        // TODO add your handling code here:
        
        // Mostra uma mensagem ao usuario para escrever seu nome e o armazena na variavel nome
        String nome = JOptionPane.showInputDialog("Qual é o seu nome?");
        
        // Instancia um objeto do tipo Jogador
        Jogador jogador = new Jogador();
        
        // Armazena ao jogador o seu nome
        jogador.setNome(nome);
        
        // Adiciona ao atributo jogador da classe jogo, a instancia de jogador
        jogo.setJogador(jogador);
        
        // Adiciona a interface gráfica o nome do jogador
        this.jogador.setText(nome);
    }//GEN-LAST:event_inserirNomeActionPerformed


