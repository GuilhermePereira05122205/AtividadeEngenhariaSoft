    private void jogarDadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jogarDadosActionPerformed
        // TODO add your handling code here:
        
        // Chama o metodo jogar para iniciar o jogo
        jogo.jogar();
        
        // Armazena os dados lançados
        Dado[] dados = jogo.getDados();
        
        // Atirbui a variavel face1 o valor da face do primeiro dado
        String face1 = String.valueOf(dados[0].getFace());
        
        // Atribui a variavel face2 0 valor da face do segundo dado
        String face2 = String.valueOf(dados[1].getFace());
        
        // Adiciona aos textos da interface grafico as faces dos dados
        valorDado1.setText(face1);
        valorDado2.setText(face2);
        
        // Armazena a soma das faces dos dados lançados
        String somaFacesDados = String.valueOf(jogo.getSomaFaces());
        
        // Adiciona a interface gráfica a soma das faces dos dados lançados
        somaFaces.setText(somaFacesDados);
        
        // Exibe o resultado do jogo
        resultado.setText(jogo.obterResultado());
        
    }//GEN-LAST:event_jogarDadosActionPerformed

